export const randomSubjectSelector = createAdvancedCyclicSelector(subjectNames);

function createAdvancedCyclicSelector(strings) {
    if (!Array.isArray(strings) || strings.length === 0) {
        throw new Error('Массив должен быть не пустым');
    }
    
    let available = [...strings];
    let used = [];
    let cycleCount = 0;
    
    const selector = {
        // Основной метод получения случайной строки
        next() {
            if (available.length === 0) {
                this.resetCycle();
                cycleCount++;
            }
            
            const randomIndex = Math.floor(Math.random() * available.length);
            const selected = available[randomIndex];
            
            available.splice(randomIndex, 1);
            used.push(selected);
            
            return selected;
        },
        
        // Сброс текущего цикла
        resetCycle() {
            available = [...used];
            used = [];
        },
        
        // Полный сброс (начать заново)
        reset() {
            available = [...strings];
            used = [];
            cycleCount = 0;
        },
        
        // Получить статистику
        getStats() {
            return {
                totalStrings: strings.length,
                availableCount: available.length,
                usedCount: used.length,
                cycleCount: cycleCount,
                isNewCycle: available.length === strings.length
            };
        },
        
        // Получить оставшиеся доступные строки
        getAvailable() {
            return [...available];
        },
        
        // Получить использованные строки в текущем цикле
        getUsed() {
            return [...used];
        }
    };
    
    return selector;
}